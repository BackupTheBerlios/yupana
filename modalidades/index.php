<h1>Modalidades</h1>

<h2>Conferencias</h2>
<p>Una conferencia aborda un tema determinado (t&eacute;cnico, filos&oacute;fico, de opini&oacute;n etc) En ella el ponente presenta el tema a desarrollar ante un auditorio. Las conferencias seran impartidas en el auditorio central de el Centro Universitario.
Se les dedica tipicamente sesiones de 1 hora (algunas 2 horas).</p>

<h2>Platicas Informales</h2>
<p>Una conferencia informal esta orientada a aquellas personas que quieran presentar algun tema, propuesta,  presentaci&oacute;n, software, etc. Estas no se rigen por el formato t&iacute;pico ponente-auditorio, es mas bien una ponencia o reuni&oacute;n entre entusiastas o interesados en el tema donde el ponente presenta su tema en una ambiente mucho mas relajado e interactivo con los asistentes. A estas se les dedica tipicamente de 1 a 2 horas.</p>

<h2>Talleres</h2>
<p>Un taller aborda a profundidad un tema t&eacute;cnico determinado, dedic&aacute;ndole una sesi&oacute;n de hasta cuatro horas, es did&aacute;ctico y pr&aacute;ctico por naturaleza. Los talleres ser&aacute;n impartidos laboratorios de c&oacute;mputo, de modo que todos los asistentes puedan ir aprendiendo y poniendo en pr&aacute;ctica de inmediato lo que el ponente est&aacute; presentando.</p>

<h2>Tutoriales</h2>
<p>Un tutorial aborda un tema tecnico determinado, dedic&aacute;ndole por lo menos una sesi&oacute;n de dos horas. Los temas muchas veces ser&aacute;n te&oacute;ricos. Los tutoriales seran impartidos en salones o laboratorios de computo donde el ponente ira presentando a los asistentes el tema tratado. Los tutoriales van a un ritmo mucho m&aacute;s agil que los talleres.</p>

<h2>Conferencias Magistrales</h2>
<p>Las conferencias magistrales ser&aacute;n impartidas por profesionales de la tecnolog&aacute;a de la informaci&oacute;n de alto nivel, lideres de opini&oacute;n o desarrolladores destacados de la comunidad del software libre.

En las conferencias magistrales se expondr&aacute;n los temas o t&oacute;picos que vayan de acuerdo al hilo central del evento.</p>

